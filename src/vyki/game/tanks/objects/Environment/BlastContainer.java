package vyki.game.tanks.objects.Environment;

import java.net.URL;

public class BlastContainer {
    private int corX = 0;
    private int corY = 0;
    private URL blastUrl;

    public int getCorX() {
        return corX;
    }

    public void setCorX(int corX) {
        this.corX = corX;
    }

    public int getCorY() {
        return corY;
    }

    public void setCorY(int corY) {
        this.corY = corY;
    }

    public URL getBlastUrl() {
        return blastUrl;
    }

    public void setBlastUrl(URL blastUrl) {
        this.blastUrl = blastUrl;
    }
}
